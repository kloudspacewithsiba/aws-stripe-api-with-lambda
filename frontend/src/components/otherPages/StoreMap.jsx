const containerStyle = {
  width: "100%",
  height: "100%",
};

export default function StoreMap({
  storesLocations,
  getLocation,
  setLocation,
}) {
  const { isLoaded } = useLoadScript({
    googleMapsApiKey: "AIzaSyAAz77U5XQuEME6TpftaMdX0bBelQxXRlM",
  });
  const calculateCenter = (locations) => {
    const numLocations = locations.length;
    const sumLat = locations.reduce((sum, loc) => sum + loc.lat, 0);
    const sumLng = locations.reduce((sum, loc) => sum + loc.lng, 0);

    return {
      lat: sumLat / numLocations,
      lng: sumLng / numLocations,
    };
  };
  const center = useMemo(() => calculateCenter(storesLocations), []);

  // add lng & lat
  const locationHandler = (location) => {
    setLocation(location);
  };

  // close handler
  const closeCardHandler = () => {
    setLocation(null);
  };

  return (
    <>
      {!isLoaded ? (
        <p>Loading...</p>
      ) : (
        <GoogleMap
          mapContainerStyle={containerStyle}
          center={center}
          zoom={3}
          options={option}
        >
          <MarkerClusterer>
            {(clusterer) =>
              storesLocations.map((marker) => (
                <Marker
                  key={marker.id}
                  position={{
                    lat: marker.lat,
                    lng: marker.lng,
                  }}
                  clusterer={clusterer}
                  onClick={() => locationHandler(marker)}
                ></Marker>
              ))
            }
          </MarkerClusterer>
          {getLocation !== null && (
            <InfoWindow
              position={{
                lat: getLocation.lat,
                lng: getLocation.lng,
              }}
              onCloseClick={closeCardHandler}
            >
              <div>
                <div className="google-map__marker-detail ">
                  <div className="google-map__marker-detail__content">
                    <h5>Store in {getLocation.city}</h5>
                    <p>
                      {getLocation.address}
                      <br />
                      {getLocation.country}
                      <br />
                      {getLocation.phone}
                      <br />
                      {getLocation.hours}
                    </p>
                  </div>
                </div>
              </div>
            </InfoWindow>
          )}
        </GoogleMap>
      )}
    </>
  );
}
